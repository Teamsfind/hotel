package com.kcsj.entitl;

public class Bumen {
	private String name;
	private int dptid;
	private int acount;
	private String time;
	private double money;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getDptid() {
		return dptid;
	}
	public void setDptid(int dptid) {
		this.dptid = dptid;
	}
	public int getAcount() {
		return acount;
	}
	public void setAcount(int acount) {
		this.acount = acount;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public double getMoney() {
		return money;
	}
	public void setMoney(double money) {
		this.money = money;
	}
}
